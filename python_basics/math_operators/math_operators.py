# Addition +
print(3 + 4)

# Subtraction -
print(3 - 4)

# Multiplication *
print(3 * 4)

# Division /
print(3 / 4)

# Floor Division //
print(9 // 2)
# Explanation: If your room is nine meters high,
# you could place four 2-meter-high wardrobes on top of each other

# Modulus %
print(9 % 2)
# Explanation: If your room is nine meters high, 
# you could place four 2-meter-high wardrobes on top of each other
# There would be one meter of vertical space left.

# Exponentiation
print( 3 ** 4)
# Explanation: 3 ** 4 is the equivalent of 3 * 3 * 3 * 3