# accessing list slices
monday_temperatures = [9.1, 8.8, 6.6, 7.5, 9.9]
print(len(monday_temperatures))

# accessing list slices from 1-4
print(monday_temperatures[1:4])

# accessing list slices from 0-2
print(monday_temperatures[0:2])

# another way to access the first two items from list
print(monday_temperatures[:2])

# accessing the last two items from list
print(monday_temperatures[3:])
