# .append() to append 9.9 another value to the list

monday_temperatures = [9.1, 8.8, 7.5]
monday_temperatures.append(9.9)
print(monday_temperatures)

# .clear() will clear the list
monday_temperatures.clear()
print(monday_temperatures)

# .index() will display the index of a value from the list
monday_temperatures = [9.1, 8.8, 7.5]
print(monday_temperatures.index(8.8))